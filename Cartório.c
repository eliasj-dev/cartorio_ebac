int registro()//Responsável por cadastrar os usuários no sistema 
{
	//Inicio criação de variáveis/string
	char arquivo[40];
	char cpf[40];
	char nome[40];
	char sobrenome[40];
	char cargo[40];
	//Final criação de variáveis/string

	printf("Digite o CPF a ser cadatrado: ");//coletando informações do usuário
	scanf("%s", cpf);//%s refere-se a string
	
	strcpy(arquivo, cpf);//Responsável por copiar os valores das strings 
	
	FILE *file; //Cria o arquivo 
	file = fopen(arquivo, "w");//Cria arquivo "w" significa escrever
	fprintf(file,cpf); // salvo o valor do arquivo
	fclose(file);// fecha o arquivo 
	
	file = fopen(arquivo, "a");//Abrir arquivo "a" significa atualizar arquivo
	fprintf(file,",");//adicionando a atualização
	fclose(file);//fechar o arquivo 
	
	printf("Digite o nome a ser cadastrado: ");//coletando informações do usuário 
	scanf("%s", nome);//"%s" refere-se a string 
	
	file = fopen(arquivo, "a");//abrir o arquivo "a" significa atualizar o arquivo 
	fprintf(file,nome);// adicionado o valor do arquivo 
	fclose(file);//fecha arquivo 
	
	file = fopen(arquivo, "a");
	fprintf(file,",");
	fclose(file);
	
	printf("Digite o sobrenome a ser cadastrado: ");
	scanf("%s", sobrenome);
	
	file = fopen(arquivo, "a");
	fprintf(file,sobrenome);
	fclose(file);
	
	file = fopen(arquivo, "a");
	fprintf(file,",");
	fclose(file); 
	
	printf("Digite o cargo a ser cadastrado: ");
	scanf("%s", cargo);
	
	file = fopen(arquivo, "a");
	fprintf(file,cargo);
	fclose(file);
	
	system("pause");
		
}

